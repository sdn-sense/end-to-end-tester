USE mysql;
GRANT ALL PRIVILEGES ON *.* TO '##ENV_MARIA_DB_USER##'@##ENV_MARIA_DB_HOST## WITH GRANT OPTION;
FLUSH PRIVILEGES;
SET PASSWORD FOR '##ENV_MARIA_DB_USER##'@##ENV_MARIA_DB_HOST## = PASSWORD("##ENV_MARIA_DB_PASSWORD##");
CREATE DATABASE IF NOT EXISTS ##ENV_MARIA_DB_DATABASE##;
CREATE USER '##ENV_GRAFANA_DB_USER##' IDENTIFIED BY "##ENV_GRAFANA_DB_PASSWORD##";
GRANT SELECT ON *.* TO '##ENV_GRAFANA_DB_USER##';
FLUSH PRIVILEGES;
SET SESSION query_cache_type = OFF;
SET SESSION TRANSACTION ISOLATION LEVEL SERIALIZABLE;